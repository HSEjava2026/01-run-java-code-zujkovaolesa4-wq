package ru.hse.lab1.second;
/**
 * Класс Person представляет человека с именем и возрастом.
 * <p>
 * Этот класс используется для хранения и обработки персональных данных.
 * Поля класса объявлены публичными для прямого доступа, что является
 * упрощенным подходом для учебных целей.
 * </p>
 *
 * @author Ваше Имя
 * @version 1.0
 * @since 2024
 */

public class Person {
    /** Имя человека. Может быть null или пустой строкой. */
    public String name;
    /**
     * Возраст человека в годах.
     * Должен быть неотрицательным числом, но в текущей реализации
     * это не проверяется в конструкторе.
     */
    public int age;
    /**
     * Создает новый объект Person с указанными параметрами.
     *
     * @param name имя человека. Может быть null.
     * @param age возраст человека в годах. Ожидается положительное значение,
     *            но в текущей реализации отрицательные значения допускаются.
     * @throws IllegalArgumentException если имя является null (в расширенной реализации)
     * @see #name
     * @see #age
     */


    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }
    /**
     * Возвращает имя человека.
     * <p>
     * Этот метод является геттером для поля {@link #name}.
     * Для изменения имени следует использовать прямое присваивание полю name,
     * так как сеттер не предоставлен.
     * </p>
     *
     * @return текущее имя человека. Может быть null.
     * @see #name
     */

    public String getName() {
        return name;
    }
    /**
     * Возвращает возраст человека.
     * <p>
     * Этот метод является геттером для поля {@link #age}.
     * Возраст измеряется в полных годах.
     * </p>
     *
     * @return текущий возраст человека в годах.
     * @see #age
     */
    public int getAge() {
        return age;
    }

}

